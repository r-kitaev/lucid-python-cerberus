__all__ = ['reversed', 'set']


try:
    reversed = reversed
except NameError:
    def reversed(an_iterable):
        copied_iterable = list(an_iterable)
        copied_iterable.reverse()
        return copied_iterable

try:
    set = set
except NameError:
    from sets import Set as set

